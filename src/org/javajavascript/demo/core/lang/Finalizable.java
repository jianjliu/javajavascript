package org.javajavascript.demo.core.lang;

import js.Js;
import jsx.Code;

public class Finalizable
{
	private final String msg;
	final Finalizable fin;

	public Finalizable(String msg, Finalizable fin) {
		this.msg = msg;
		this.fin = fin;
	}

	@Override
	protected void finalize() {
		Js.alert(Code.join(msg, " ", "finalized!"));
	}
}
